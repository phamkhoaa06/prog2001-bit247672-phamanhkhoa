#include <stdio.h>

int main() {
    float pi = 3.14159;

    // Mặc định 6 chữ số thập phân
    printf("Mac dinh: %f\n", pi);

    // Lấy 1 chữ số thập phân
    printf("1 chu so thap phan: %.1f\n", pi);

    // Lấy 4 chữ số thập phân
    printf("4 chu so thap phan: %.4f\n", pi);

    return 0;
}
